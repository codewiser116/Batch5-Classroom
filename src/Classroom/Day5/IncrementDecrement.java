package Classroom.Day5;

public class IncrementDecrement {
    public static void main(String[] args) {
        int number = 10;

        number++; // 10+1 = 11
        System.out.println(number); // 11
        number++; // 11+1 = 12
        System.out.println(number); // 12
        number--; // 12-1 = 11
        System.out.println(number); // 11
        number--;
        number--;
        number--;
        number--;
        number--;
        number--;
        System.out.println(number); // 5


    }
}
