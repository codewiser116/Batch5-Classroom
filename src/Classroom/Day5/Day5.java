package Classroom.Day5;

public class Day5 {
    public static void main(String[] args) {
        // The following code will print: Hello World 20235!
        System.out.println("Hello " + " World " + 2023 + 5 + "!" );


        byte byte1 = 5, byte2 = 10, byte3 = 15;
        short short1 = 10, short2 = 5000, short3 = 32300;
        // Bytes: someByteValue, someByteValue, someByteValue
        // Shorts: someShortValue, someShortValue, someShortValue
        System.out.println("Bytes: " + byte1 + ", " + byte2 + ", " + byte3);
        System.out.println("Shorts: " + short1 + ", " + short2 + ", " + short3);
    }
}
