package Classroom.Day5;

public class CompoundOperator {
    public static void main(String[] args) {
        int number = 10;
        // 1.
        number = number + 15; // number = 10 + 20
        System.out.println(number);

        number+=15; // number = 25 + 15
        System.out.println(number);

    }
}
